    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();
            initTable();
        } catch (Exception ex) {
            Logger.getLogger(EditPublisher.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfFirstName, tfLastName, tfBirthDate, tfPicturePath);
        errorLabels = Arrays.asList(lbNameError, lbLastNameError, lbBirthDateError, lbPicturePathError);
    }

    private void hideErrors() {
        errorLabels.forEach(e -> e.setVisible(false));
    }

    private void initRepository() throws Exception {
        repository = RepositoryFactory.getRepository(PublisherRepository.class);
    }

    private void initTable() throws Exception {
        tbPublishers.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbPublishers.setAutoCreateRowSorter(true);
        tbPublishers.setRowHeight(25);
        publisherTableModel = new PublisherTableModel(repository.selectPublishers());
        tbPublishers.setModel(publisherTableModel);
    }

    private boolean formValid() {
        hideErrors();
        boolean ok = true;
        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setVisible(validationFields.get(i).getText().trim().isEmpty());
            if ("Date".equals(validationFields.get(i).getName())) {
                try {
                    System.out.println("Trying to parse: '" + validationFields.get(i).getText().trim() + "'");
                    LocalDate.parse(validationFields.get(i).getText().trim(), Publisher.DATE_FORMATTER);
                } catch (Exception e) {
                    ok = false;
                    errorLabels.get(i).setVisible(true);
                }
            }
        }
        return ok;
    }

    private void clearForm() {
        hideErrors();
        validationFields.forEach(e -> e.setText(""));
        lbIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/no_image.png")));
        selectedPublisher = null;
    }

    private String uploadPicture() throws IOException {
        String picturePath = tfPicturePath.getText();
        String ext = picturePath.substring(picturePath.lastIndexOf("."));
        String pictureName = UUID.randomUUID() + ext;
        String localPicturePath = DIR + File.separator + pictureName;

        FileUtils.copy(picturePath, localPicturePath);
        return localPicturePath;
    }

    private void showPublisher() {
        clearForm();
        int selectedRow = tbPublishers.getSelectedRow();
        int rowIndex = tbPublishers.convertRowIndexToModel(selectedRow);
        int selectedPublisherId = (int) publisherTableModel.getValueAt(rowIndex, 0);

        try {
            Optional<Publisher> publisherOptional = repository.selectPublisher(selectedPublisherId);
            if (publisherOptional.isPresent()) {
                selectedPublisher = publisherOptional.get();
                fillForm(selectedPublisher);
            }
        } catch (Exception ex) {
            Logger.getLogger(EditPublisher.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to show publisher!");
        }
    }

    private void fillForm(Publisher publisher) {
        if (publisher.getPicturePath() != null && Files.exists(Paths.get(publisher.getPicturePath()))) {
            tfPicturePath.setText(publisher.getPicturePath());
            setIcon(lbIcon, new File(publisher.getPicturePath()));
        }
        tfFirstName.setText(publisher.getFirstName());
        tfLastName.setText(publisher.getLastName());
        tfBirthDate.setText(publisher.getDateBirth().format(Publisher.DATE_FORMATTER));
    }


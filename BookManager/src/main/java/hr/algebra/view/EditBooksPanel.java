    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();
            initTable();
        } catch (Exception ex) {
            Logger.getLogger(EditBooksPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfTitle, tfLink, taDescription, tfPublishedDate, tfPicturePath);
        errorLabels = Arrays.asList(lbTitleError, lbLinkError, lbDescriptionError, lbPublishedDateError, lbPicturePathError);
    }

    private void hideErrors() {
        errorLabels.forEach(e -> e.setVisible(false));
    }

    private void initRepository() throws Exception {
        repository = RepositoryFactory.getRepository(BookRepository.class);
    }

    private void initTable() throws Exception {
        tbBooks.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbBooks.setAutoCreateRowSorter(true);
        tbBooks.setRowHeight(25);
        booksTableModel = new BookTableModel(repository.selectBooks());
        tbBooks.setModel(booksTableModel);
    }

    private boolean formValid() {
        hideErrors();
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setVisible(validationFields.get(i).getText().trim().isEmpty());
            if ("Date".equals(validationFields.get(i).getName())) {
                try {
                    LocalDateTime.parse(validationFields.get(i).getText().trim(), Book.DATE_FORMATTER);
                } catch (Exception e) {
                    ok = false;
                    errorLabels.get(i).setVisible(true);
                }
            }
        }
        return ok;
    }

    private void clearForm() {
        hideErrors();
        validationFields.forEach(e -> e.setText(""));
        lbIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/no_image.png")));
        selectedBook = null;
    }

    private String uploadPicture() throws IOException {
        String picturePath = tfPicturePath.getText();
        String ext = picturePath.substring(picturePath.lastIndexOf("."));
        String pictureName = UUID.randomUUID() + ext;
        String localPicturePath = DIR + File.separator + pictureName;

        FileUtils.copy(picturePath, localPicturePath);
        return localPicturePath;
    }

    private void showBook() {
        clearForm();
        int selectedRow = tbBooks.getSelectedRow();
        int rowIndex = tbBooks.convertRowIndexToModel(selectedRow);
        int selectedBookId = (int) booksTableModel.getValueAt(rowIndex, 0);

        try {
            Optional<Book> optBook = repository.selectBook(selectedBookId);
            if (optBook.isPresent()) {
                selectedBook = optBook.get();
                fillForm(selectedBook);
            }
        } catch (Exception ex) {
            Logger.getLogger(EditBooksPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to show book!");
        }
    }

    private void fillForm(Book book) {
        if (book.getPicturePath() != null && Files.exists(Paths.get(book.getPicturePath()))) {
            tfPicturePath.setText(book.getPicturePath());
            setIcon(lbIcon, new File(book.getPicturePath()));
        }
        tfTitle.setText(book.getTitle());
        tfLink.setText(book.getLink());
        taDescription.setText(book.getDescription());
        tfPublishedDate.setText(book.getPublishedDate().format(Book.DATE_FORMATTER));
    }


    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!formValid()) {
            return;
        }
        try {
            String localPicturePath = uploadPicture();
            Publisher publisher = new Publisher(
                    tfFirstName.getText().trim(),
                    tfLastName.getText().trim(),
                    LocalDate.parse(tfBirthDate.getText().trim(), Publisher.DATE_FORMATTER),
                    localPicturePath
            );
            repository.createPublisher(publisher);
            publisherTableModel.setPublishers(repository.selectPublishers());

            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(EditPublisher.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to create publisher!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedPublisher == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose publisher to update");
            return;
        }
        if (!formValid()) {
            return;
        }
        try {
            if (!tfPicturePath.getText().trim().equals(selectedPublisher.getPicturePath())) {
                if (selectedPublisher.getPicturePath() != null) {
                    Files.deleteIfExists(Paths.get(selectedPublisher.getPicturePath()));
                }
                String localPicturePath = uploadPicture();
                selectedPublisher.setPicturePath(localPicturePath);
            }

            selectedPublisher.setFirstName(tfFirstName.getText().trim());
            selectedPublisher.setLastName(tfLastName.getText().trim());
            selectedPublisher.setDateBirth(LocalDate.parse(tfBirthDate.getText().trim(), Publisher.DATE_FORMATTER));

            repository.updatePublisher(selectedPublisher.getId(), selectedPublisher);
            publisherTableModel.setPublishers(repository.selectPublishers());
            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(EditPublisher.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to update publisher!");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedPublisher == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose publisher to delete");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete publisher",
                "Do you really want to delete publisher?")) {
            try {
                if (selectedPublisher.getPicturePath() != null) {
                    Files.deleteIfExists(Paths.get(selectedPublisher.getPicturePath()));
                }
                repository.deletePublisher(selectedPublisher.getId());
                publisherTableModel.setPublishers(repository.selectPublishers());

                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(EditPublisher.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete publisher!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


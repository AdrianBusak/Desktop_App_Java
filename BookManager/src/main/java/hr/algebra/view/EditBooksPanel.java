    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!formValid()) {
            return;
        }
        try {
            String localPicturePath = uploadPicture();
            Book book = new Book(
                    tfTitle.getText().trim(),
                    tfLink.getText().trim(),
                    taDescription.getText().trim(),
                    LocalDateTime.parse(tfPublishedDate.getText().trim(), Book.DATE_FORMATTER),
                    localPicturePath
            );
            repository.createBook(book);
            booksTableModel.setBooks(repository.selectBooks());

            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(EditBooksPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to create book!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedBook == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose book to update");
            return;
        }
        if (!formValid()) {
            return;
        }
        try {
            if (!tfPicturePath.getText().trim().equals(selectedBook.getPicturePath())) {
                if (selectedBook.getPicturePath() != null) {
                    Files.deleteIfExists(Paths.get(selectedBook.getPicturePath()));
                }
                String localPicturePath = uploadPicture();
                selectedBook.setPicturePath(localPicturePath);
            }

            selectedBook.setTitle(tfTitle.getText().trim());
            selectedBook.setLink(tfLink.getText().trim());
            selectedBook.setDescription(taDescription.getText().trim());
            selectedBook.setPublishedDate(LocalDateTime.parse(tfPublishedDate.getText().trim(), Book.DATE_FORMATTER));

            repository.updateBook(selectedBook.getId(), selectedBook);
            booksTableModel.setBooks(repository.selectBooks());

            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(EditBooksPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to update book!");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedBook == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose book to delete");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete book",
                "Do you really want to delete book?")) {
            try {
                if (selectedBook.getPicturePath() != null) {
                    Files.deleteIfExists(Paths.get(selectedBook.getPicturePath()));
                }
                repository.deleteBook(selectedBook.getId());
                booksTableModel.setBooks(repository.selectBooks());

                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(EditBooksPanel.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete book!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

